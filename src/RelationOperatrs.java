public class RelationOperatrs {
    public static void main (String[] args){

        int i1 = 11;
        int i2 = 19;
        System.out.println(i1 < i2);
        System.out.println(i1 > i2);
        System.out.println(i1 <= i2);
        System.out.println(i1 >= i2);
        System.out.println(i1 == i2);
        System.out.println(i1 != i2);
        // All the above works only for one condition
    }
}
